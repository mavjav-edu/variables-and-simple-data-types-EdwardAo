their_name=' \njoe\t '  # \t is a tab character
print('normal:游릭'+their_name+'游댮')  # print a string with a variable in it
print('lstrip:游릭'+their_name.lstrip()+'游댮')  # print the string again, but with the whitespace on the left side stripped 
print('rstrip:游릭'+their_name.rstrip()+'游댮') # printing variable with rstrip
print('strip:游릭'+their_name.strip()+'游댮')# printing variable with strip